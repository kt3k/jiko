import { assertEquals } from "@std/assert";
import { convertCoordinate } from "./codemap.ts";

function assertAlmostEquals(actual: number, expected: number, msg: string) {
  const diff = Math.abs(actual - expected);
  assertEquals(
    diff < 1e-6,
    true,
    `${msg}: expected ${expected}, got ${actual} (diff: ${diff})`,
  );
}

